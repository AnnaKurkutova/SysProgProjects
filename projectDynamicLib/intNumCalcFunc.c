#include "intNumCalcFunc.h"

//Операция сложения двух целых чисел. 

int  addIntegers(int firstValue, int secondValue){
	int resultValue;

	resultValue = firstValue + secondValue;
	return resultValue;
}

//Операция вычитания одного целого числа из другого.

int subtractIntegers(int firstValue, int secondValue){
	int resultValue;

	resultValue = firstValue - secondValue;
	return resultValue;
}

//Операция умножения двух целых чисел. 

int multiplyIntegers(int firstValue, int secondValue){
	int resultValue;

	resultValue = firstValue * secondValue;
	return resultValue;
}

//Операция деления одного целого числа на другое.

int divideIntegers(int firstValue, int secondValue){
	int resultValue;

	resultValue = firstValue / secondValue;
	return resultValue;
}
